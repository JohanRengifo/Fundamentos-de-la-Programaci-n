class cicloPara
{
    public static void main(String args[])
    {
        //Declara ka Como entero y como 1
        //Mientras K sea menor o Igual a 10 Se cumple el ciclo
        //K va en encremento de 1 a 1
        for(int k=1; k<=10; k++)
        {
            System.out.println("Numero " + k);
        }   
    }
}
